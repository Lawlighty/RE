# 匹配一段文本中所有的身份证数字。18位
import re
s = 'sfafsf,34234234234,1231313132,154785625475896587,sdefgr54184785ds85,4864465asf86845'
p = '[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]'
m = re.findall(p, s)
print(m)


s = str(input('>输入身份证'))
p = '^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$'
if re.match(p, s):
    print('来真的?')
else:
    print('滚蛋')